·温迪吹散冰雪，建立新蒙德

旧的神座崩毁，新神诞生。风神巴巴托斯体会到了指尖流淌的力量。他用这力量所做的第一件事，便是以少年的模样重塑自身形体。——因为只有用人类的形体，才能弹好少年钟爱的竖琴。他拂动琴弦，用神风吹散冰雪，劈开山峦。让新的蒙德成为自由之地吧，成为无人称王的国度。假以时日，应该会是很好、很浪漫的城邦…

